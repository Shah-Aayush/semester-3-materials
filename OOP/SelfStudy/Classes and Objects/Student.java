public class Student {
		int id,age;
		String name;
		public int getId(){
			return id;
		}
		public int getAge(){
			return age;
		}
		public String getName(){
			return name;
		}
		public void setId(int id){
			this.id = id;
		}
		public void setAge(int age){
			this.age = age;
		}
		public void setName(String name){
			this.name = name;
		}
}