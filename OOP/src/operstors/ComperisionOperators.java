package operstors;

public class ComperisionOperators {
	public static void main(String[] args)
	{
//		comperision operators returns only booliean values i.e true or false
//		and here = -> assignment operators
//		== -> comperision operators
		int a = 3;
		int b = 5;
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
		
		
	}

}
